import math

numero = 25
raiz = math.sqrt(numero)   # raiz cuadrada
print(raiz)

numero_pi = math.pi
print(math.pi)

print(round(numero_pi, 2))  # redondea a dos decimales

numero = 6.758423

numero_pi = math.ceil(numero)  # redondea hacia arriba 6.758423 -> 7
print(numero_pi)

numero_pi = math.floor(numero)  # redondea hacia abajo 6.758423 -> 6
print(numero_pi)
